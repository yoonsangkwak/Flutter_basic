void main() {
  int a = 1;
  int b = 3;

  print('a + b : ${a + b}');
  print('a - b : ${a - b}');
  print('a * b : ${a * b}');
  print('a / b : ${a / b}');
  print('a ~/ b : ${a ~/ b}');
  print('~a : ${~a}');
  print("");

  bool x = true;
  bool y = false;

  print(x || y);
  print(x && y);
  print(!x);
  print("");

  print(1 > 2);
  print(1 == 2);
}
